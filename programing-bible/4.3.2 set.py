# 集合的操作:

# 交集
A = {1, 2, 3, 4}
B = {3, 4, 5, 6}
print(A & B) # {3, 4}

# 聯集
C = {1, 2, 3, 4}
D = {3, 4, 5, 6}
print(A | B) # {1, 2, 3, 4, 5, 6}

# 差集
E = {1, 2, 3, 4}
F = {3, 4, 5, 6}
print(A - B) # {1, 2}
print(B - A) # {5, 6}

# 對稱差集
G = {1, 2, 3, 4}
H = {3, 4, 5, 6}
print(A ^ B) # {1, 2, 5, 6}

# 等於 ==

# 不等於 !=

# 是成員 in

# 不是成員 not in
I = {1, 2, 3, 4}
print(2 in I) # True
print(2 not in I) # False